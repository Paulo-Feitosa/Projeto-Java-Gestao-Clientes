    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        DaoCliente clienteDao = new DaoCliente();
        
        Cliente cliente = clienteDao.getClienteById(Integer.parseInt(txtId.getText()));
        
        if(cliente != null){
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date data = cliente.getDataNascimento();
            try {
                data = sdf.parse("dd/MM/yyyy");
            } catch (ParseException ex) {
            }
            txtNome.enable(true);
            txtCpf.enable(true);
            txtNascimento.enable(true);
            txtEndereco.enable(true);
            txtTelefone.enable(true);
            
            txtNome.setText(cliente.getNome());
            txtCpf.setText(Integer.toString(cliente.getCpf()));
            txtNascimento.setText(sdf.format(data));
            txtEndereco.setText(cliente.getEndereco());
            txtTelefone.setText(Integer.toString(cliente.getTelefone()));
        }
        else{
            JOptionPane.showMessageDialog(null, "Occoreu um erro ao realizar opera√ß√£o");
        }
    }//GEN-LAST:event_BuscarActionPerformed

    private void SalvarEdicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarEdicaoActionPerformed
        if(txtNome.isEnabled()){
            DaoCliente clienteDao = new DaoCliente();
            Cliente cliente = new Cliente();
            
            cliente.setId(Integer.parseInt(txtId.getText()));
            cliente.setCpf(Integer.parseInt(txtCpf.getText()));
            cliente.setNome(txtNome.getText());
            try {
               cliente.setDataNascimento(new SimpleDateFormat("dd/MM/yyyy").parse(txtNascimento.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(NovoCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            cliente.setEndereco(txtEndereco.getText());
            cliente.setTelefone(Integer.parseInt(txtTelefone.getText()));
            
            if(clienteDao.PutCliente(cliente)){
                JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso");
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao realiza opera√ß√£o!");
                this.dispose();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "busque um cliente antes de salvar!");
        }
    }//GEN-LAST:event_SalvarEdicaoActionPerformed

