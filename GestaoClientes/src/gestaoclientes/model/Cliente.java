package gestaoclientes.model;

import java.util.Date;

/**
 *
 * @author Bruno
 * @see Classe de persistência para guardar propriedades e métodos relacionados a Cliente
 */
public class Cliente {
    private int id;
    private String nome;
    private int cpf;
    private Date dataNascimento;
    private String endereco;
    private int telefone;

    public Cliente() {
    }

    public Cliente(int id, String nome, int cpf, Date dataNascimento, String endereco, int telefone) {
        this.id = id;
        this.nome = nome;
        this.cpf = cpf;
        this.dataNascimento = dataNascimento;
        this.endereco = endereco;
        this.telefone = telefone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public Date getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(Date dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }
    
    
}